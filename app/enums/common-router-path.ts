/**
 * 通用路由地址枚举
 */
enum CommonRouterPath {
  /**
   * 首页
   */
  HOME = '/home',
  /**
   * 登录页
   */
  LOGIN = '/login',
  /**
   * 404页
   */
  NOT_FOUND = '/404',
  /**
   * 403页
   */
  FORBIDDEN = '/403',
  /**
   * 重定向页面
   */
  REDIRECT = '/redirect',
  /**
   * 起始页
   */
  START = '/start',
}

export default CommonRouterPath;
